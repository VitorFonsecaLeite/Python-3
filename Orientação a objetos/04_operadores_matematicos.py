''' Os operadores são utilizados para equações matematicas :
Adição= + 
Subtração= - 
Divisão= /   
Multiplicação= * 
Potencia= **
Divisão inteira= //  
Resto de divisão= %'''
# ----------------------------------------------------------------
n1 = 5 + 2
n2 = 5 - 2
n3 = 5 * 2
n4 = 5 / 2
n5 = 5 ** 2
n6 = 5 // 2
n7 = 5 % 2

print(' 5 + 2=', n1, 'Adição')
print(' 5 - 2=', n2, 'Subtração')
print(' 5 * 2=', n3, 'Multiplicação')
print(' 5 / 2=', n4, 'Divisão')
print(' 5 ** 2=', n5, 'Potencia')
print(' 5 // 2=', n6, 'Divisão inteira')
print(' 5 % 2=', n7, 'Resto de divisão')
