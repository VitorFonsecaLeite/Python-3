''' FOR : loop que percorre sequencias, 
    repetindo ações para cada elelmento, 
    desconsiderando o ultimo numero de elementos '''

for x in range(5):
    print(x) # na 1º repetição x (variavel de controle) equivale a 0, na 2º a 1 ......

for c in range(5,11): # Assim infoemamos o inicio e fim da contagem.
    print(c)

for n in range(11,0,-1):# Adicionamos um 3º elemento para interagir com o loop
    print(n)

print('----------------------------------------------------------------------------------')